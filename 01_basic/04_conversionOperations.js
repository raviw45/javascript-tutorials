// incremental and decremental operators
let x=20;
console.log(`x:${x}`);
let y=++x;
console.log(`x:${x}, y:${y}`);
let z=y++;
console.log(`x:${x}, y:${y}, z:${z}`);

z=--z;
console.log(`z:${z++}`)
console.log(`Modified z:${z}`)